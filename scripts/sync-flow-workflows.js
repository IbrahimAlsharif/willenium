#!/usr/bin/env node

const fs = require("fs");
const path = require("path");

const GENERATED_MARKER = "# This file is generated by scripts/sync-flow-workflows.js. Do not edit manually.";
const repoRoot = path.resolve(__dirname, "..");
const pomPath = path.join(repoRoot, "pom.xml");
const workflowsDir = path.join(repoRoot, ".github", "workflows");

function exitWithError(message) {
  console.error(`Error: ${message}`);
  process.exit(1);
}

function toWorkflowFileName(profileId) {
  return (
    profileId
      .replace(/([a-z0-9])([A-Z])/g, "$1-$2")
      .replace(/[^A-Za-z0-9]+/g, "-")
      .replace(/-+/g, "-")
      .replace(/^-|-$/g, "")
      .toLowerCase() + ".yml"
  );
}

function parseProfiles(pomXml) {
  const profiles = [];
  const profilePattern = /<profile>([\s\S]*?)<\/profile>/g;
  let match = profilePattern.exec(pomXml);

  while (match) {
    const block = match[1];
    const idMatch = block.match(/<id>([^<]+)<\/id>/);
    const suiteMatch = block.match(/<suiteXmlFile>([^<]+)<\/suiteXmlFile>/);

    if (idMatch && suiteMatch) {
      profiles.push({
        profileId: idMatch[1].trim(),
        suiteXmlFile: suiteMatch[1].trim()
      });
    }

    match = profilePattern.exec(pomXml);
  }

  return profiles.sort((left, right) => left.profileId.localeCompare(right.profileId));
}

function isApiProfile({ profileId, suiteXmlFile }) {
  return /api/i.test(profileId) || /api/i.test(suiteXmlFile);
}

function findExistingWorkflowFilesByName() {
  const fileNamesByWorkflowName = new Map();

  if (!fs.existsSync(workflowsDir)) {
    return fileNamesByWorkflowName;
  }

  for (const fileName of fs.readdirSync(workflowsDir)) {
    if (!fileName.endsWith(".yml")) {
      continue;
    }

    const workflowPath = path.join(workflowsDir, fileName);
    const content = fs.readFileSync(workflowPath, "utf8");
    const nameMatch = content.match(/^name:\s*(.+)$/m);

    if (!nameMatch) {
      continue;
    }

    const workflowName = nameMatch[1].trim().replace(/^['"]|['"]$/g, "");
    fileNamesByWorkflowName.set(workflowName, fileName);
  }

  return fileNamesByWorkflowName;
}

function buildUiWorkflow({ profileId, suiteXmlFile }) {
  return `${GENERATED_MARKER}
# Source profile: ${profileId}
# Source suite: ${suiteXmlFile}
name: ${profileId}

on:
  workflow_dispatch:

jobs:
  test:
    runs-on: ubuntu-latest
    services:
      selenium:
        image: selenium/standalone-chrome:4.1.0
        ports:
          - 4444:4444
    strategy:
      matrix:
        os: [mac]
        browser: [chrome]
    steps:
      - name: Checkout code
        uses: actions/checkout@v3

      - name: Set up JDK 11
        uses: actions/setup-java@v4
        with:
          java-version: '11'
          distribution: 'temurin'
          cache: maven

      - name: Install Xvfb
        run: |
          sudo apt-get update
          sudo apt-get install -y xvfb
          Xvfb :99 -screen 0 1920x1080x24 &

      - name: Set DISPLAY environment variable
        run: echo "DISPLAY=:99" >> $GITHUB_ENV

      - name: Run Selenium Tests
        run: mvn clean test -P ${profileId} -DremoteDriverUrl=http://selenium:4444/wd/hub -Dos=\${{ matrix.os }} -Dbrowser=\${{ matrix.browser }}

      - name: Upload test screenshots
        if: always()
        uses: actions/upload-artifact@v4
        with:
          name: test-screenshots
          path: ./screenshots/

      - name: Upload test report
        if: always()
        uses: actions/upload-artifact@v4
        with:
          name: test-report
          path: ./extent-reports/
`;
}

function buildApiWorkflow({ profileId, suiteXmlFile }) {
  return `${GENERATED_MARKER}
# Source profile: ${profileId}
# Source suite: ${suiteXmlFile}
name: ${profileId}

on:
  workflow_dispatch:

jobs:
  test:
    runs-on: ubuntu-latest
    steps:
      - name: Checkout code
        uses: actions/checkout@v3

      - name: Set up JDK 11
        uses: actions/setup-java@v4
        with:
          java-version: '11'
          distribution: 'temurin'
          cache: maven

      - name: Run API Tests
        run: mvn clean test -P ${profileId}

      - name: Upload test report
        if: always()
        uses: actions/upload-artifact@v4
        with:
          name: test-report
          path: ./extent-reports/
`;
}

function buildWorkflow(profile) {
  return isApiProfile(profile) ? buildApiWorkflow(profile) : buildUiWorkflow(profile);
}

function syncWorkflows() {
  if (!fs.existsSync(pomPath)) {
    exitWithError(`Missing pom.xml at ${pomPath}`);
  }

  const pomXml = fs.readFileSync(pomPath, "utf8");
  const profiles = parseProfiles(pomXml);

  if (profiles.length === 0) {
    exitWithError("No Maven profiles with suiteXmlFile entries were found in pom.xml.");
  }

  fs.mkdirSync(workflowsDir, { recursive: true });

  const existingWorkflowFiles = findExistingWorkflowFilesByName();
  const expectedFiles = new Set();

  for (const profile of profiles) {
    const fileName =
      existingWorkflowFiles.get(profile.profileId) || toWorkflowFileName(profile.profileId);
    const workflowPath = path.join(workflowsDir, fileName);

    fs.writeFileSync(workflowPath, buildWorkflow(profile));
    expectedFiles.add(fileName);
  }

  for (const fileName of fs.readdirSync(workflowsDir)) {
    if (!fileName.endsWith(".yml")) {
      continue;
    }

    const workflowPath = path.join(workflowsDir, fileName);
    const currentContent = fs.readFileSync(workflowPath, "utf8");
    const isGenerated = currentContent.startsWith(GENERATED_MARKER);

    if (isGenerated && !expectedFiles.has(fileName)) {
      fs.rmSync(workflowPath);
    }
  }

  console.log(`Synced ${expectedFiles.size} GitHub workflow file(s) from pom.xml profiles.`);
}

syncWorkflows();
